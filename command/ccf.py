def strip_js_comments(text: str) -> str:
    result: list[str] = []
    i = 0
    quote = ""
    escaped = False
    while i < len(text):
        ch = text[i]
        nxt = text[i + 1] if i + 1 < len(text) else ""
        if quote:
            result.append(ch)
            if escaped:
                escaped = False
            elif ch == "\\":
                escaped = True
            elif ch == quote:
                quote = ""
            i += 1
            continue
        if ch in {"'", '"', "`"}:
            quote = ch
            result.append(ch)
            i += 1
            continue
        if ch == "/" and nxt == "/":
            i += 2
            while i < len(text) and text[i] not in "\r\n":
                i += 1
            continue
        if ch == "/" and nxt == "*":
            i += 2
            while i + 1 < len(text) and not (text[i] == "*" and text[i + 1] == "/"):
                i += 1
            i += 2
            continue
        result.append(ch)
        i += 1
    return "".join(result)


def extract_balanced(text: str, start: int, open_ch: str, close_ch: str) -> str:
    depth = 0
    quote = ""
    escaped = False
    for i in range(start, len(text)):
        ch = text[i]
        if quote:
            if escaped:
                escaped = False
            elif ch == "\\":
                escaped = True
            elif ch == quote:
                quote = ""
            continue
        if ch in {"'", '"', "`"}:
            quote = ch
            continue
        if ch == open_ch:
            depth += 1
        elif ch == close_ch:
            depth -= 1
            if depth == 0:
                return text[start + 1:i]
    raise ValueError("未找到完整的 JS 数组或对象")


def split_top_level_items(text: str) -> list[str]:
    items: list[str] = []
    start = 0
    depth = 0
    quote = ""
    escaped = False
    for i, ch in enumerate(text):
        if quote:
            if escaped:
                escaped = False
            elif ch == "\\":
                escaped = True
            elif ch == quote:
                quote = ""
            continue
        if ch in {"'", '"', "`"}:
            quote = ch
            continue
        if ch in "[{(":
            depth += 1
        elif ch in "]})":
            depth -= 1
        elif ch == "," and depth == 0:
            item = text[start:i].strip()
            if item:
                items.append(item)
            start = i + 1
    item = text[start:].strip()
    if item:
        items.append(item)
    return items


def js_string_value(value: str) -> str:
    value = value.strip()
    if len(value) >= 2 and value[0] in {"'", '"', "`"} and value[-1] == value[0]:
        return value[1:-1].replace("\\'", "'").replace('\\"', '"').replace("\\`", "`").replace("\\n", "\n")
    return value


def extract_property(body: str, name: str) -> str | None:
    match = re.search(rf"(?:^|[,\s]){re.escape(name)}\s*:", body)
    if not match:
        return None
    colon = body.find(":", match.start())
    pos = colon + 1
    while pos < len(body) and body[pos].isspace():
        pos += 1
    if pos >= len(body):
        return None
    if body[pos] == "[":
        return extract_balanced(body, pos, "[", "]")
    if body[pos] == "{":
        return extract_balanced(body, pos, "{", "}")
    quote = body[pos] if body[pos] in {"'", '"', "`"} else ""
    escaped = False
    start = pos + 1 if quote else pos
    for i in range(start, len(body)):
        ch = body[i]
        if quote:
            if escaped:
                escaped = False
            elif ch == "\\":
                escaped = True
            elif ch == quote:
                return body[pos:i + 1]
            continue
        if ch == ",":
            return body[pos:i].strip()
    return body[pos:].strip()


def parse_string_array(value: str | None) -> list[str]:
    if not value:
        return []
    result: list[str] = []
    for item in split_top_level_items(value):
        item = item.strip()
        if item and item[0] in {"'", '"', "`"}:
            result.append(js_string_value(item))
    return result


def parse_number_array(value: str | None) -> list[int]:
    if not value:
        return []
    return [int(item) for item in re.findall(r"\d+", value)]


